def transform(v87):
    result = 0
    for i in range(8):
        result = (result << 1) | ((v87 >> i) & 1)
    return result
# 示例
v87 = 0x63
print(hex(transform(v87)))  # 输出: 7
v87 = 0xc6
print(hex(transform(v87)))  # 输出: 7 对称加密


